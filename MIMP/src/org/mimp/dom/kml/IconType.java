package org.mimp.dom.kml;

public class IconType {

    String href;
    
    public IconType() {
        
    }

    public IconType(String href) {
        this.href = href;
    }

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }
}
